#include <string.h>
#include <stdlib.h>
#include "../include/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*result;

	if (count * size < size)
		return (NULL);
	result = malloc(count * size);
	if (!result)
		return (NULL);
	else
	{
		ft_bzero(result, (count * size));
		return (result);
	}
}
